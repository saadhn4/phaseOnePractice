/*

2. Variables & Data Types
- Declare variables of all primitive data types in JavaScript and log their types using `typeof`.

*/

let a = "string";
console.log(a, typeof a);

let b = 5;
console.log(b, typeof b);

let c = true;
console.log(c, typeof c);

let d = null;
console.log(d, typeof d);

let e = undefined;
console.log(e, typeof e);
